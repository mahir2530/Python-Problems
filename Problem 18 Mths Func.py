import math  # Import the math module for mathematical functions

# Basic arithmetic functions
print(abs(-10))  # Returns the absolute value of -10. 
# Output: 10
print(pow(2, 3))  # Returns 2 raised to the power of 3. 
# Output: 8
print(round(3.14159, 2))  # Rounds 3.14159 to 2 decimal places. 
# Output: 3.14

# Exponential and logarithmic functions
print(math.exp(2))  # Returns e^2 (where e is the base of natural logarithms). 
# Output: 7.3890560989306495
print(math.log(10))  # Returns the natural logarithm of 10 (base e). 
# Output: 2.302585092994046
print(math.log10(100))  # Returns the base-10 logarithm of 100. 
# Output: 2.0

# Trigonometric functions
print(math.sin(math.radians(90)))  # Returns the sine of 90 degrees.
# Output: 1.0
print(math.degrees(math.asin(1)))  # Returns the inverse sine of 1, converted to degrees. 
# Output: 90.0

# Special functions
print(math.sqrt(25))  # Returns the square root of 25.
# Output: 5.0
print(math.factorial(5))  # Returns the factorial of 5 (5!).
# Output: 120
