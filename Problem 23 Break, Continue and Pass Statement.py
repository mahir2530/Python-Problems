
print('====BREAK====')
for letter in 'Python':
    if letter == 'h':
        break  
    print("Letter:", letter)

print('====CONTINUE====')
for letter in 'Python':
    if letter == 'h':
        continue  
    print("Letter:", letter)

print('====PASS====')
for letter in 'Python':
    if letter == 'h':
        pass  
    print("Letter:", letter)
